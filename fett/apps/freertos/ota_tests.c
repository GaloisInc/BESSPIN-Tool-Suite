void test_ed25519_verify(ed25519_key *pk)
{
  int signature_ok;
  int r;

  // TEST CASE 1 - GOOD SIGNATURE
  fettPrintf ("Test case 1 - expected good signature\n");

  // good_one points at the signature (64 bytes) followed directly by the message
  r = wc_ed25519_verify_msg((byte *) good_one,  // ptr to first byte of signature
                            ED25519_SIG_SIZE,   // size of signature

                            good_one + ED25519_SIG_SIZE,     // ptr to first byte of message
                            PAYLOAD_SIZE - ED25519_SIG_SIZE, // size of message

                            &signature_ok,            // Returned status
                            pk);                      // public key
  (void) r;
  if (signature_ok == 1)
    {
      fettPrintf ("Signature is OK - PASS\n");
    }
  else
    {
      fettPrintf ("Signature is NOT OK - FAIL\n");
    }

  
  fettPrintf ("Test case 2 - corrupt signature\n");

  r = wc_ed25519_verify_msg((byte *) bad_sig,  // ptr to first byte of signature
                            ED25519_SIG_SIZE,  // size of signature

                            bad_sig + ED25519_SIG_SIZE,     // ptr to first byte of message
                            PAYLOAD_SIZE - ED25519_SIG_SIZE, // size of message

                            &signature_ok,            // Returned status
                            pk);                      // public key
  if (signature_ok == 1)
    {
      fettPrintf ("Signature is OK - FAIL\n");
    }
  else
    {
      fettPrintf ("Signature is NOT OK - PASS\n");
    }

  fettPrintf ("Test case 3 - corrupt message\n");

  r = wc_ed25519_verify_msg((byte *) bad_msg,  // ptr to first byte of signature
                            ED25519_SIG_SIZE,  // size of signature

                            bad_msg + ED25519_SIG_SIZE,     // ptr to first byte of message
                            PAYLOAD_SIZE - ED25519_SIG_SIZE, // size of message

                            &signature_ok,            // Returned status
                            pk);                      // public key
  if (signature_ok == 1)
    {
      fettPrintf ("Signature is OK - FAIL\n");
    }
  else
    {
      fettPrintf ("Signature is NOT OK - PASS\n");
    }

  fettPrintf ("Test case 4 - corrupt message and signature\n");

  r = wc_ed25519_verify_msg((byte *) bad_both,  // ptr to first byte of signature
                            ED25519_SIG_SIZE,  // size of signature

                            bad_both + ED25519_SIG_SIZE,     // ptr to first byte of message
                            PAYLOAD_SIZE - ED25519_SIG_SIZE, // size of message

                            &signature_ok,            // Returned status
                            pk);                      // public key
  if (signature_ok == 1)
    {
      fettPrintf ("Signature is OK - FAIL\n");
    }
  else
    {
      fettPrintf ("Signature is NOT OK - PASS\n");
    }
}
